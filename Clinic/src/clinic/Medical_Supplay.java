public class Medical_Supplay {
    double price;
    int serial_num;
   public  String name;
    String supplier;
    
    public Medical_Supplay() {
    }

    public Medical_Supplay(double price, int serial_num, String name, String supplier) {
        this.price = price;
        this.serial_num = serial_num;
        this.name = name;
        this.supplier = supplier;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getSerial_num() {
        return serial_num;
    }

    public void setSerial_num(int serial_num) {
        this.serial_num = serial_num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSupplier() {
        return supplier;
    }

    public void setSupplier(String supplier) {
        this.supplier = supplier;
    }
    
    
}